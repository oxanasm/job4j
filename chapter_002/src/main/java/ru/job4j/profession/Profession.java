package ru.job4j.profession;

/**
 * Родительский класс профессия, содержит: имя, профессия, услуга
 */

public class Profession {
    private String name;

    private String profession;

    public Profession() {

    }

    public String getName() {
        return name;
    }

    public String getProfession() {
        return profession;
    }

}